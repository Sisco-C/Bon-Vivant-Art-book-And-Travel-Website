    	<h4 style="font-style: italic;">The Places Untraversed Series</h4>
    	<p>I always knew and felt it since I was little: I never really belonged anywhere. So I figured, if I ever got to grow up and get a more prominent bossom, I would sure go away.

My name is Lady Piazza, a name I picked up from a novel (She was a dog in there) I wasnt supposed to be reading in the first place. My mom used to call them books of the grown folk, while my dad was happy I was getting into books that actually mirrored what he wanted me to be when I was old enough. My dad, a man so gentle and far more traditional, wanted grandkids while in all our confrontations with him- which were not so often, it always seemed to him that I was going to be what he dreaded most: a loner.

As an all-time feeble child, I spent all my time in my head. Casually browsing the round thing placed atop my neck for answers that even books could not give. “Be jovial, play with the kids, this will be your home forever, so you need to make friends around here” was my parents’ everyday anthem. But deep beneath, I was scared that I knew the truth. The fear had called forth the silence, and I picked the mute kiddo title, that was bestowed upon me by the neighbour who always made sure that the block smelt of whiskies.

All through lower school and upper primary, I lived off my genius. I was smart, and so the bullying was not quite a thing that came my way. Of all my drawbacks, at least there was something I’d lay claim on; I would hold the title so close to my chest over the years as I transitioned from a gawky-faced fat teenager into a woman who learnt to finally stop questioning every single thing. Why people breathed? Why people didnt get her quirkiness, why her crackhead mom often yelled at the dishes while she did them, or why all the other children around her did not seem so engrossed in the not-so-important aspects of their lives like she was. The school library. It was many years later, while trying to solo-raise Perriwinkle, that I would come to understand what I was at the time: a polymath.

The first time I got to London, I was about 26, drenched in the hopes of things getting better and completely moving away from people that knew me. I wanted to be invisible, to stand in a forest, and completely camouflage with it, but soon I realised that I would never do that. That my mind would never align with no one else’s, and that I needed to go find a place that I would. It was during one of the many nights that I spent trying to calm my hazy mind, that the thought came to my cerebra(sic). It came so easily that I unshackled the unbelievabilities of my head. It could be so lonely and dark at times, that I wanted to pluck it and throw it away. I could live without a head, I thought,maybe without it, life would be easier, and better and I would not easily shrug it off when someone offered to help. On other days, Lady Piazza’s head resembled that of Zadie Smith – so messy and chaotic, that even the smallest of thought, could reshuffle it, after days on end of putting it in order.

And so, when I finally had the balls to put it into fruition, I called my one friend and told her I would be gone. I wanted her to know that my going away, did not have anything to do with her inability to find me deep beneath where I for so long had wanted her to. Instead, I did realise that her friendship was all I had left around, and as a good person, I was indebted to cut ties. It was my major trait. Slicing people off my life, like they did not belong there, something I had taken off the potatoes in our countryside farm. And then, I flew away. In the skies, far above the practicalities of my daily routine, I tried to keep both my nausea and my emotions level. Even though I often loathed this place that I was leaving probably for good, I knew that it would always stay with me. That even though I ran, there was some ties to this place I wouldn’t sever, like an umblical cord to a child: Always reminding it that it came from somewhere, and that that somewhere would never get erased. Even with the strongest of erasers.

Settling into my newfound escape-home did not prove much of a hassle. My skin is like tanned hot chocolate and that might have been a major thing to consider before moving, but I was glad I was now somewhere I could stay anonymous. The streets, packed with so many strange faces, was predominantly white. You would think I was scared to be so alone in some foregn place, but it weirdly felt safe. People were nice to each other, or so it seemed, and no one really seemed to care that the other’s shorts were too little. All I had armed for this new and scary, but fulfilling escapade, was Candice William’s book Queenie. I knew the blocks, the places that were populated with people like me, in case I badly needed community. I also knew what it somehow meant to be a black girl with a conspicouos booty in this strangely familiar city. That book was all I needed. My mean face would have to be slackened a little.

And many many years later, having lived in a sub-let apartment with people I would end up calling home, I let myself accept that I was not braced for all that came my way. That London was not the heaven I so much adored in books, or the place my little naive mind had fashioned it to be. That so much about this place could not be written about, but could only be felt. I fathomed London to be my forever home, the place I would live off my spinster years and then get to fourty and then seventy and then maybe just fade into the skies. Because a spirit like mine didn’t die off, it flew like the ravens and planted itself into some other little child, just like it had me.

But then, that would not happen, ’cause ole habits die hard. I was always a goer, and would forever be, till the very end when the heart waves would die down into a straight line. And as Perriwinkle clears the apartment of her mother’s things, she finally understands why her mother was who she was, always moving away, always in search of things that were a tad more exhilarating. Her mother, so tired of life itseld, never wanted to live in the confines of familiarities!

I left London.</p>
