 		<h3>Bali In My Head ~ As Written by Sisco C.</h3>
 		<hr>
 		<hr>

 		<div class="row">
  <div class="column">

 		<p>I am hurled up in my bed right now, with my baby laptop in tow and my little pillow almost giving in to the weight of my bottoms. I am watching a documentary and listening to some of Allan jackson’s country music, which is practically impossible. I hate that I am the only (two) person around me who listens to them, and then I start to drift.

I am in Bali, clad in little shorts and an oversized tee , my earphones plugged in. The rice fields are green, greener than the tea plantation outside my father’s house.The streets are calm and the Indonesian waters are shimmering in the evening light. The sun graciously blesses the horizons while I stare into the edges of the water. My heart races, and I keep exclaiming, ‘Wow, this is so cute!’. The palms are beautiful and their reflections in the sorrounding water pools are to die for. I am crying.


I flush out my phone and take random pictures, in faith that it wouldn’t fall in the waters. I am in a water-space,and the way the falls are coming down, I cant hold it in anymore, so I walk up to this tour-in-charge guy. His name is Kigen, definately not a native of the country, but whose love for the oceans is unparallelled. He offers me a mangosteen he was having and I cant eat it! What is this joy that I am feeling? I am overwhelmed and I feel like a little kid who just got their first toy. Kigen is nice, a normal black guy, with a British accent and a tempting physique.

‘Yo I just cant figure you out, you’re black, with a Britsh undertone, and living in Bali, can you tell me how you teleport?’ His first instinct was to laugh. I sounded like a dumbass but I just wanted to be like him, to travel, to feel the world, to let loose, to trek barefoot, but most of all, to be what I am, a wanderluster. So he jerked out this little book he had in his pocket, dubbed The Peniless Wanderluster and whose author had a funny name; Sisko Cherokee. Things felt crazier. I was living in a world I wrote about many years ago, and I did not expect it to feel this way. I remembered writing the articles in my room, and how many times I erased lines because I thought that I was overrating experiences I’d never felt and might just never feel. The times that I excluded my soulmate from the picture because I would never make up a whole person just for a story. I shrieked, I stared, I saw it. Perhaps this was the fire to my ice, the seal to my cracks and the coins to my escapades. And in the moment, everything joned up, Bali was our fairytale and now here we were, miles away from our little bedsitter in Juja, feeding in the scenery with a man who masqueraded as a guide. My heart is full, and we’re on our next flight to Costa Rica.

I just cant seem to get over the sambal guling, probably because that is where everything made perfect sense. I had made up my own world, and I was living in it. Here was me traversing seas and sands, wining and dining, skidding and walking. Writing books and drinking cold coffee with my person switching between painting and playing the cello, and the strings kept reminding me of the lines we hanged clothes on while in the campus hostels. The world is my home now, and the mountains my bedroom.

Saturdays are for overthinking and random story-writing, oversleeping and overeating, over-ly sending of risky texts and heavy cursing when you are left on read. But most of all, days like this belong to peniless wanderlusters!</p>
